using System;

namespace CallingMethodsAssignment
{
    // This class contains three math methods
    public class MathOperations
    {
        // Method 1: Adds 10 to the number
        public int AddTen(int number)
        {
            return number + 10;
        }

        // Method 2: Multiplies the number by 5
        public int MultiplyByFive(int number)
        {
            return number * 5;
        }

        // Method 3: Subtracts 3 from the number
        public int SubtractThree(int number)
        {
            return number - 3;
        }
    }
}
